public class Operators {
    public static void main(String[] args) {
        // Unary Operators

        // Increment Opertors
        // Pre Increment
        // int a = 10;
        // int b = ++a;
        // System.out.println(a); // 11
        // System.out.println(b); // 11

        // Post Increment
        int a = 10;
        int b = a++;
        System.out.println(a); // 11
        System.out.println(b); // 10

        // Decrement Opertors
        // Pre Decrement
        // int c = 10;
        // int d = --c;
        // System.out.println(c); // 9
        // System.out.println(d); // 9

        // Post Decrement
        int c = 10;
        int d = c--;
        System.out.println(c); // 9
        System.out.println(d); // 10
    }
}